class Solution:
    def specialArray(self, nums: list) -> int:
        nums.sort()
        for i in range(0, max(nums) + 1):
            n = 0
            for j in range(len(nums)):
                if nums[j] >= i:
                    n += 1
            if i == n:
                return i
        return -1


