from typing import List


class Solution:
    def minimalKSum(self, nums: List[int], k: int) -> int:
        nums.sort()
        summ = 0
        n = 1

        def range_sum(x, y):
            return int((y -x +1) * (x + y) /2)

        if n < nums[0]:
            if k > (nums[0] - n):
                summ += range_sum(1, nums[0] -1)
                k -= (nums[0] - n)
            else:
                summ += range_sum(1, k)
                return summ
        for i in range(len(nums) -1):
            if nums[i+1] - nums[i] > 1:
                if nums[i+1] - nums[i] - 1 < k:
                    summ += range_sum(nums[i]+1, nums[i+1] - 1)
                    k -= nums[i+1] - nums[i] - 1
                else:
                    summ += range_sum(nums[i] + 1, nums[i] + k)
                    return summ
        summ += range_sum(nums[-1] + 1, nums[-1] + k)
        return summ

    def minimalKSum2(self, nums: List[int], k: int) -> int:
        nums.sort()
        n = 1
        summ = 0
        while n < nums[0]:
            summ += n
            n += 1
            k -= 1
            if k == 0:
                break
        n = nums[0] + 1
        for i in range(len(nums) -1):
            if nums[i+1] - nums[i] > 1:
                diff = nums[i+1] - nums[i]
                n = nums[i] + 1
                while diff > 1 and k > 0:
                    summ += n
                    n += 1
                    k -= 1
                    diff -= 1
                    # if k == 0:
                    #     break
            if k == 0:
                break

        n = nums[-1] + 1
        while k > 0:
            summ += n
            n += 1
            k -= 1
        return summ

